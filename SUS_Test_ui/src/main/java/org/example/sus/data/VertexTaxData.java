public class VertexTaxData {

    public String ref;
    public String opCoID;
    public String opCoName;
    public String accountName;
    public String supportRequestNumber;
    public String vertexTaxTX;
    public String vertexTaxLA;
    public String vertexTaxNM;

    public String getRef() {
        return ref;
    }

    public void setRef(String ref) {
        this.ref = ref;
    }

    public String getOpCoID() {
        return opCoID;
    }

    public void setOpCoID(String opCoID) {
        this.opCoID = opCoID;
    }

    public String getOpCoName() {
        return opCoName;
    }

    public void setOpCoName(String opCoName) {
        this.opCoName = opCoName;
    }

    public String getAccountName() {
        return accountName;
    }

    public void setAccountName(String accountName) {
        this.accountName = accountName;
    }

    public String getSupportRequestNumber() {
        return supportRequestNumber;
    }

    public void setSupportRequestNumber(String supportRequestNumber) {
        this.supportRequestNumber = supportRequestNumber;
    }

    public String getVertexTaxTX() {
        return vertexTaxTX;
    }

    public void setVertexTaxTX(String vertexTaxTX) {
        this.vertexTaxTX = vertexTaxTX;
    }

    public String getVertexTaxLA() {
        return vertexTaxLA;
    }

    public void setVertexTaxLA(String vertexTaxLA) {
        this.vertexTaxLA = vertexTaxLA;
    }

    public String getVertexTaxNM() {
        return vertexTaxNM;
    }

    public void setVertexTaxNM(String vertexTaxNM) {
        this.vertexTaxNM = vertexTaxNM;
    }
}
